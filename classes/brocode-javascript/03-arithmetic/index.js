let students = 30;

// + plus
students += 1;
// students = students + 1;
console.log(students);

// - minus
students -= 1;
// students = students - 1;
console.log(students);

// * multiply
students *= 2;
// students = students * 2;
console.log(students);

// / divide
students /= 2;
// students = students / 2;
console.log(students);

// ** exponent
students **= 2;
// students = students ** 2
console.log(students);

// % modulus
students %= 2;
// students = students % 2
console.log(students);

// ++ increment
students++;
console.log(students);

// -- decrement
students--;
console.log(students);


// --- operator precedence --- 
//     PEMDAS
//     Parentheses()
//     Exponents
//     Multiplication, Division, Modulus
//     Addition, Subtraction
